package nl.naturalis.purl;

public class PurlConfigException extends PurlException {

  public PurlConfigException(String message) {
    super(message);
  }

  public PurlConfigException(Throwable cause) {
    super(cause);
  }

  public PurlConfigException(String message, Throwable cause) {
    super(message, cause);
  }

}
